#include<stdio.h>
#define MAXSIZE 1048576
long long tmp[MAXSIZE];
long long int belt[MAXSIZE];
void transmission(long long int belt[],int N,int T) {
    if(T == 0) return;
    else {

        if(T >= 64) {
            int shift = (T/64)%N; /* shift = 1 ; shift = the number of belt to be shifted; %N to make it fast */
            for(int i = 0; i < N; i++)
                tmp[i] = belt[i];
            for(int i  = 0; i < N; i++) 
                belt[i] = tmp[(i+shift <= N-1) ? (i+shift) : (i+shift-N)];
        }

        int step_to_shift = T % 64; /* check whether T is a multiple of 64 */
        if(step_to_shift > 0) { /* T is not a multiple of 64 */
            long long int mask = 1, first_bits_to_save, second_bits_to_save;
            for(int i = 1; i < step_to_shift; i++) 
                mask = (mask<<1) | (long long int)1;

            first_bits_to_save = (belt[0] >> (64-step_to_shift)) & mask;
            belt[0] = (belt[0] << step_to_shift);

            for(int j = 1; j < N; j++) {
                //save the 
                second_bits_to_save = (belt[j] >> (64-step_to_shift)) & mask;
                belt[j-1] = (belt[j-1] | second_bits_to_save);
                belt[j] = (belt[j] << step_to_shift);
            }

            belt[N-1] = (belt[N-1] | first_bits_to_save); 
        }
        return;
    }
}


int main(){
    int N,T;
    scanf("%d%d",&N,&T);
    for(int i=0;i<N;i++)
        scanf("%lld",&belt[i]);
    transmission(belt,N,T);
    for(int i=0;i<N;i++)
        printf("%lld%s",belt[i],i==N-1?"":" ");
    return 0;
}

